<?php
namespace Eksmo\Cinema\Model;

/**
 * @method static Cinema create()
 */
class Cinema extends AbstractModel
{
    public static $_table = 'cinema';

    /**
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param string $name
     * @return $this
     */
    public function setName($name)
    {
        $this->name = $name;
        return $this;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }
}